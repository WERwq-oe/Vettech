/** Spatial hash for neighbor queries */
class SpatialHash {
    constructor(cellSize) {
        this.cellSize = cellSize;
        this.map = new Map();
    }
    _key(x, y) { return `${x}|${y}`; }
    _cellFor(pos) { return [Math.floor(pos.x / this.cellSize), Math.floor(pos.y / this.cellSize)]; }
    clear() { this.map.clear(); }
    insert(particle) {
        const [cx, cy] = this._cellFor(particle.position);
        const key = this._key(cx, cy);
        let list = this.map.get(key);
        if (!list) { list = []; this.map.set(key, list); }
        list.push(particle);
    }
    neighbors(pos, radius) {
        const r = radius;
        const [cx, cy] = this._cellFor(pos);
        const res = [];
        for (let y = cy - 1; y <= cy + 1; y++) {
            for (let x = cx - 1; x <= cx + 1; x++) {
                const list = this.map.get(this._key(x, y));
                if (!list) continue;
                for (const p of list) {
                    if (Math.abs(p.position.x - pos.x) <= r && Math.abs(p.position.y - pos.y) <= r) {
                        res.push(p);
                    }
                }
            }
        }
        return res;
    }
}

/** Particle representation */
class Particle {
    constructor(x, y, size, hue) {
        this.position = new Vec2(x, y);
        this.velocity = new Vec2();
        this.acceleration = new Vec2();
        this.home = new Vec2(x, y);
        this.size = size;
        this.hue = hue; // base hue for color ramp
        this.alpha = 0; // fade in
        this.mass = clamp(size * 0.12, 0.6, 2.2);
        this.id = 0;
    }
    integrate(dt) {
        this.velocity.add(this.acceleration.scale(dt));
        // drag
        this.velocity.scale(0.98);
        this.position.add(this.velocity.copy().scale(dt));
        this.acceleration.set(0, 0);
    }
    applyForce(fx, fy) { this.acceleration.x += fx / this.mass; this.acceleration.y += fy / this.mass; }
}

/** Particle system with morphing */
class ParticleSystem {
    constructor(canvas) {
        this.canvas = canvas;
        this.ctx = canvas.getContext('2d');
        this.dpr = Math.max(1, Math.min(2.5, window.devicePixelRatio || 1));
        this.particles = [];
        this.hash = new SpatialHash(22);
        this.rng = new RNG(42);
        this.noise = new SimplexLikeNoise(123);
        this.pointer = { x: 0, y: 0, isDown: false };
        this.sizeBase = 2.0;
        this.hueBase = 170; // cyan/green
        this.time = 0;
        this.targetPoints = [];
        this.targetBounds = { w: 1, h: 1 };
        this.morphStrength = 0.10; // how strongly particles move towards targets
        this.maxParticles = 4800; // adaptive based on screen
        this.fade = 0.12; // trail
        this.qualityScale = 1.0; // dynamically adjust for FPS
        this.lastFpsCheck = performance.now();
        this.frames = 0;
        this.fps = 60;
        this._onResize = () => this.resize();
        this._onPointerMove = (e) => this.pointerMove(e);
        this._onPointerDown = () => { this.pointer.isDown = true; };
        this._onPointerUp = () => { this.pointer.isDown = false; };
        window.addEventListener('resize', this._onResize);
        window.addEventListener('pointermove', this._onPointerMove);
        window.addEventListener('pointerdown', this._onPointerDown);
        window.addEventListener('pointerup', this._onPointerUp);
        this.resize();
        this.populate();
    }

    resize() {
        const scale = this.dpr;
        const width = Math.floor(this.canvas.clientWidth || this.canvas.parentElement.clientWidth);
        const height = Math.floor(this.canvas.clientHeight || this.canvas.parentElement.clientHeight);
        this.canvas.width = Math.max(1, Math.floor(width * scale));
        this.canvas.height = Math.max(1, Math.floor(height * scale));
        this.ctx.setTransform(scale, 0, 0, scale, 0, 0);
        this.hash = new SpatialHash(20);
        // adjust particle count to area
        const area = width * height;
        const targetCount = clamp(Math.floor(area / 120), 1200, prefersReduced ? 1400 : this.maxParticles);
        if (targetCount !== this.particles.length) {
            this.populate(targetCount);
        }
    }

    populate(count) {
        const width = this.canvas.width / this.dpr;
        const height = this.canvas.height / this.dpr;
        const desired = count ?? clamp(Math.floor((width * height) / 120), 1200, prefersReduced ? 1400 : this.maxParticles);
        const diff = desired - this.particles.length;
        if (diff > 0) {
            for (let i = 0; i < diff; i++) {
                const x = this.rng.range(0, width);
                const y = this.rng.range(0, height);
                const size = this.sizeBase + this.rng.range(-0.6, 1.1);
                const hue = this.hueBase + this.rng.range(-24, 24);
                const p = new Particle(x, y, size, hue);
                p.id = this.particles.length + i;
                p.alpha = 0;
                this.particles.push(p);
            }
        } else if (diff < 0) {
            this.particles.splice(desired);
        }
    }

    pointerMove(e) {
        const rect = this.canvas.getBoundingClientRect();
        this.pointer.x = (e.clientX - rect.left);
        this.pointer.y = (e.clientY - rect.top);
    }

    setTargets(points, bounds) {
        this.targetPoints = points;
        this.targetBounds = bounds;
    }

    step(dt) {
        this.time += dt;
        this.frames++;
        const now = performance.now();
        if (now - this.lastFpsCheck > 1000) {
            this.fps = this.frames * 1000 / (now - this.lastFpsCheck);
            this.lastFpsCheck = now; this.frames = 0;
            // adapt quality
            const target = this.fps > 55 ? 1.0 : this.fps > 40 ? 0.9 : this.fps > 30 ? 0.75 : 0.6;
            this.qualityScale = lerp(this.qualityScale, target, 0.2);
        }

        // rebuild spatial hash
        this.hash.clear();
        for (const p of this.particles) this.hash.insert(p);

        const w = this.canvas.width / this.dpr;
        const h = this.canvas.height / this.dpr;
        const pointer = new Vec2(this.pointer.x, this.pointer.y);
        const hasTargets = this.targetPoints && this.targetPoints.length > 0;

        // choose subset of target points for efficiency
        const targetCount = Math.min(this.particles.length, Math.floor(this.targetPoints.length * this.qualityScale) || 0);
        const spacing = hasTargets ? Math.max(1, Math.floor(this.targetPoints.length / Math.max(1, targetCount))) : 1;

        for (let i = 0; i < this.particles.length; i++) {
            const p = this.particles[i];

            // morph towards targets using mapped index
            if (hasTargets && targetCount > 0) {
                const tIndex = (i * spacing) % this.targetPoints.length;
                const target = this.targetPoints[tIndex];
                const steer = new Vec2(target.x - p.position.x, target.y - p.position.y);
                const dist = Math.max(0.001, steer.length());
                steer.normalize().scale(this.morphStrength * 60);
                p.applyForce(steer.x, steer.y);
                // arrive behavior slows down near target
                const arrive = clamp(dist / 120, 0.08, 1.0);
                p.velocity.scale(0.9 + 0.1 * arrive);
            } else {
                // wander around home
                const drift = 0.4 + 0.3 * Math.sin((i % 10) + this.time * 0.6);
                const n = this.noise.noise((p.position.x + i * 7) * 0.003, (p.position.y - i * 3) * 0.003);
                p.applyForce(Math.cos(n * Math.PI * 2) * drift, Math.sin(n * Math.PI * 2) * drift);
            }

            // pointer interaction
            const dx = p.position.x - pointer.x;
            const dy = p.position.y - pointer.y;
            const d2 = dx * dx + dy * dy;
            const r = 120 * (this.pointer.isDown ? 1.6 : 1);
            if (d2 < r * r) {
                const f = this.pointer.isDown ? -60 : 24; // attract on hold, repel when hovering
                const inv = 1 / Math.max(24, Math.sqrt(d2));
                p.applyForce(dx * inv * f, dy * inv * f);
            }

            // edge bounds
            if (p.position.x < -10 || p.position.x > w + 10) { p.velocity.x *= -0.6; p.position.x = clamp(p.position.x, 0, w); }
            if (p.position.y < -10 || p.position.y > h + 10) { p.velocity.y *= -0.6; p.position.y = clamp(p.position.y, 0, h); }

            // subtle gravity well center
            const gx = (w * 0.5 - p.position.x) * 0.0008;
            const gy = (h * 0.5 - p.position.y) * 0.0008;
            p.applyForce(gx, gy);

            // alpha fade in
            p.alpha = Math.min(1, p.alpha + 0.015);

            p.integrate(dt);
        }
    }

    draw() {
        const ctx = this.ctx;
        const w = this.canvas.width / this.dpr;
        const h = this.canvas.height / this.dpr;
        ctx.save();
        // motion trails
        ctx.globalAlpha = prefersReduced ? 1 : (1 - this.fade);
        ctx.fillStyle = '#04050a';
        ctx.fillRect(0, 0, w, h);
        ctx.globalAlpha = 1;

        // composite blending for glow
        ctx.globalCompositeOperation = 'lighter';
        for (let i = 0; i < this.particles.length; i++) {
            const p = this.particles[i];
            const alpha = p.alpha * 0.8 * this.qualityScale;
            ctx.fillStyle = `hsla(${p.hue}, 90%, 62%, ${alpha})`;
            ctx.beginPath();
            ctx.arc(p.position.x, p.position.y, p.size, 0, Math.PI * 2);
            ctx.fill();
        }
        ctx.globalCompositeOperation = 'source-over';
        ctx.restore();
    }
}

