/** Clamp a number between min and max */
export function clamp(value, min, max) {
    return Math.min(max, Math.max(min, value));
}

/** Linear interpolation */
export function lerp(a, b, t) {
    return a + (b - a) * t;
}

/** Smoothstep easing */
export function smoothstep(edge0, edge1, x) {
    const t = clamp((x - edge0) / (edge1 - edge0), 0, 1);
    return t * t * (3 - 2 * t);
}

/** Exponential ease out */
export function easeOutExpo(t) {
    return t === 1 ? 1 : 1 - Math.pow(2, -10 * t);
}

/**
 * Pseudo-random generator with seed for deterministic effects
 */
export class RNG {
    constructor(seed = 123456789) {
        this.seed = seed >>> 0;
    }
    next() {
        // xorshift32
        let x = this.seed;
        x ^= x << 13; x ^= x >>> 17; x ^= x << 5;
        this.seed = x >>> 0;
        return this.seed / 0xFFFFFFFF;
    }
    range(min, max) {
        return min + (max - min) * this.next();
    }
    pick(array) {
        return array[Math.floor(this.range(0, array.length))];
    }
}

/** 2D vector class for geometry math */
export class Vec2 {
    constructor(x = 0, y = 0) { this.x = x; this.y = y; }
    copy() { return new Vec2(this.x, this.y); }
    set(x, y) { this.x = x; this.y = y; return this; }
    add(v) { this.x += v.x; this.y += v.y; return this; }
    sub(v) { this.x -= v.x; this.y -= v.y; return this; }
    scale(s) { this.x *= s; this.y *= s; return this; }
    length() { return Math.hypot(this.x, this.y); }
    normalize() { const l = this.length() || 1; this.x /= l; this.y /= l; return this; }
    distance(v) { return Math.hypot(this.x - v.x, this.y - v.y); }
    lerp(v, t) { this.x = lerp(this.x, v.x, t); this.y = lerp(this.y, v.y, t); return this; }
}

/**
 * Tiny perlin-like noise for organic motion
 * Not a full implementation; sufficient for subtle drift
 */
export class SimplexLikeNoise {
    constructor(seed = 1337) {
        this.rng = new RNG(seed);
        this.perm = new Uint8Array(512);
        for (let i = 0; i < 256; i++) this.perm[i] = i;
        // shuffle
        for (let i = 255; i > 0; i--) {
            const j = Math.floor(this.rng.range(0, i + 1));
            [this.perm[i], this.perm[j]] = [this.perm[j], this.perm[i]];
        }
        this.perm.set(this.perm.slice(0,256), 256);
    }
    fade(t) { return t * t * t * (t * (t * 6 - 15) + 10); }
    grad(hash, x, y) { const h = hash & 3; return (h === 0 ? x : h === 1 ? -x : h === 2 ? y : -y); }
    noise(x, y) {
        const X = Math.floor(x) & 255;
        const Y = Math.floor(y) & 255;
        x -= Math.floor(x);
        y -= Math.floor(y);
        const u = this.fade(x), v = this.fade(y);
        const A = this.perm[X] + Y, B = this.perm[X+1] + Y;
        const n00 = this.grad(this.perm[A], x, y);
        const n01 = this.grad(this.perm[A+1], x, y-1);
        const n10 = this.grad(this.perm[B], x-1, y);
        const n11 = this.grad(this.perm[B+1], x-1, y-1);
        const ix0 = lerp(n00, n10, u);
        const ix1 = lerp(n01, n11, u);
        return lerp(ix0, ix1, v);
    }
}

