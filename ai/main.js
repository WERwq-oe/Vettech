class Starfield {
    constructor(canvas) {
        this.canvas = canvas;
        this.ctx = canvas.getContext('2d');
        this.stars = [];
        this.dpr = Math.max(1, window.devicePixelRatio || 1);
        this.populate();
        window.addEventListener('resize', () => this.populate());
    }
    populate() {
        const w = this.canvas.width / engine.dpr;
        const h = this.canvas.height / engine.dpr;
        const count = clamp(Math.floor((w * h) / 8000), 40, 220);
        this.stars = new Array(count).fill(0).map(() => ({
            x: Math.random() * w,
            y: Math.random() * h,
            z: Math.random() * 0.6 + 0.4,
            tw: Math.random() * 2,
        }));
    }
    draw() {
        const w = this.canvas.width / engine.dpr;
        const h = this.canvas.height / engine.dpr;
        const ctx = this.ctx;
        ctx.save();
        ctx.globalCompositeOperation = 'screen';
        for (const s of this.stars) {
            const a = 0.25 + 0.55 * Math.abs(Math.sin(performance.now() * 0.001 * s.tw));
            ctx.fillStyle = `rgba(180, 240, 255, ${a * s.z * 0.6})`;
            ctx.fillRect(s.x, s.y, 1, 1);
        }
        ctx.restore();
    }
}

