/** Render text to offscreen canvas and return sample points */
class TextSampler {
    constructor() {
        this.canvas = document.createElement('canvas');
        this.ctx = this.canvas.getContext('2d');
        this.dpr = Math.max(1, window.devicePixelRatio || 1);
    }
    sample(text, options = {}) {
        const { width = 900, height = 300, font = '900 220px Inter, system-ui', letterSpacing = 0 } = options;
        const scale = this.dpr;
        this.canvas.width = Math.floor(width * scale);
        this.canvas.height = Math.floor(height * scale);
        const ctx = this.ctx;
        ctx.setTransform(scale, 0, 0, scale, 0, 0);
        ctx.clearRect(0, 0, width, height);
        ctx.fillStyle = '#fff';
        ctx.textAlign = 'center';
        ctx.textBaseline = 'middle';
        ctx.font = font;
        if (letterSpacing !== 0) ctx.letterSpacing = `${letterSpacing}px`;
        ctx.fillText(text, width / 2, height / 2);
        const imageData = ctx.getImageData(0, 0, width, height).data;
        const points = [];
        const gap = 6; // sampling step
        for (let y = 0; y < height; y += gap) {
            for (let x = 0; x < width; x += gap) {
                const idx = (y * width + x) * 4 + 3;
                if (imageData[idx] > 200) points.push({ x, y });
            }
        }
        return { points, bounds: { w: width, h: height } };
    }
}

/** Parametric shapes for morphing */
class ShapeSampler {
    circle(radius, count) {
        const pts = [];
        for (let i = 0; i < count; i++) {
            const a = (i / count) * Math.PI * 2;
            pts.push({ x: Math.cos(a) * radius + radius, y: Math.sin(a) * radius + radius });
        }
        return { points: pts, bounds: { w: radius * 2, h: radius * 2 } };
    }
    lissajous(a = 3, b = 2, count = 180, scale = 180) {
        const pts = [];
        for (let i = 0; i < count; i++) {
            const t = (i / count) * Math.PI * 2;
            pts.push({ x: Math.sin(a * t) * scale + scale, y: Math.sin(b * t) * scale + scale });
        }
        return { points: pts, bounds: { w: scale * 2, h: scale * 2 } };
    }
}

/* =============================
   Page transition controller
   ============================= */

class PageTransition {
    constructor(el) {
        this.el = el;
        this.y = 1; // 1 => offscreen bottom
        this.v = 0;
        this.target = 1;
    }
    open() { this.target = 0; }
    close() { this.target = 1; }
    update(dt) {
        // critically damped spring
        const stiffness = 120;
        const damping = 16;
        const force = stiffness * (this.target - this.y) - damping * this.v;
        this.v += force * dt;
        this.y += this.v * dt;
        if (Math.abs(this.target - this.y) < 0.001 && Math.abs(this.v) < 0.001) { this.y = this.target; this.v = 0; }
        this.el.style.transform = `translateY(${this.y * 100}%)`;
    }
}

