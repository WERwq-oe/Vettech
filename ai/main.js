export const Easing = {
    linear: t => t,
    quadIn: t => t * t,
    quadOut: t => t * (2 - t),
    cubicIn: t => t * t * t,
    cubicOut: t => --t * t * t + 1,
    quartIn: t => t * t * t * t,
    quartOut: t => 1 - (--t) * t * t * t,
    sineIn: t => 1 - Math.cos((t * Math.PI) / 2),
    sineOut: t => Math.sin((t * Math.PI) / 2),
    expoIn: t => (t === 0 ? 0 : Math.pow(2, 10 * t - 10)),
    expoOut: t => (t === 1 ? 1 : 1 - Math.pow(2, -10 * t)),
};

export class Emitter {
    constructor() { this.map = new Map(); }
    on(evt, cb) { if (!this.map.has(evt)) this.map.set(evt, new Set()); this.map.get(evt).add(cb); return () => this.off(evt, cb); }
    off(evt, cb) { const set = this.map.get(evt); if (!set) return; set.delete(cb); }
    emit(evt, data) { const set = this.map.get(evt); if (!set) return; for (const cb of set) cb(data); }
}

export const Color = {
    hslToRgb(h, s, l) {
        s /= 100; l /= 100; h /= 360;
        const k = n => (n + h * 12) % 12;
        const a = s * Math.min(l, 1 - l);
        const f = n => l - a * Math.max(-1, Math.min(k(n) - 3, Math.min(9 - k(n), 1)));
        return [f(0)*255, f(8)*255, f(4)*255];
    },
    rgbToHex(r,g,b){ return '#' + [r,g,b].map(v=>Math.round(v).toString(16).padStart(2,'0')).join(''); }
};

